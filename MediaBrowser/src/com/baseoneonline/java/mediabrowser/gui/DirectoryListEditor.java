	public void setDirectories(File[] mediaSources) {
		directories.clear();
		for (File dir : mediaSources) {
			directories.add(dir);
		}
	}

	private void fireDataChanged() {
		for (Listener l : listeners) {
			l.dataChanged();
		}
	}

