	private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
		JFileChooser fc = new JFileChooser();
		fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
		if (JFileChooser.APPROVE_OPTION == fc.showOpenDialog(this)) {
			File dir = fc.getSelectedFile();
			directories.add(dir);
			fireDataChanged();
		}
	}//GEN-LAST:event_btAddActionPerformed

	private void listDirectoriesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listDirectoriesValueChanged
		boolean hasSelection = listDirectories.getSelectedIndices().length > 0;
		btRemove.setEnabled(hasSelection);
	}//GEN-LAST:event_listDirectoriesValueChanged

	private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
		for (Object dir : listDirectories.getSelectedValues()) {
			directories.remove((File)dir);
		}
		fireDataChanged();
	}//GEN-LAST:event_btRemoveActionPerformed


